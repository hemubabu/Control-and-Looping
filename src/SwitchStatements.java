
public class SwitchStatements {
	public static void main(String[] args) {
		String day = "SUNday";
		switch(day) {
		case "Monday":
			System.out.println("Equal");
			break;
		case "Tuesday":
			System.out.println("Equal");
			break;
		case "Wednesday":
			System.out.println("Not Equal");
			break;
		default:
			System.out.println("Invalid");	
	}
		int i=1;
		do {
			System.out.println(i);
			i++;
		}
		while(i<10);
		
			
		
}
}
