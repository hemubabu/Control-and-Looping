package Statements;

public class While {
	
	public static void main(String[] args) {
		for(int i=0;i<10;i++) {
			System.out.println("FOR LOOP");
		}
		
		
		
		
		int j=20; 	
		while(j<20) {			
			System.out.println(j);			
			j++;			
		}
		System.out.println("While Loop ended");
		
		
		
		
		
		
		int k =20;
		do {
			System.out.println(k);
			k++;			
		}
		while(k<20);
		System.out.println("Do while Loop ended");	
	}

}
